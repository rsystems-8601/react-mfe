// Web 1.0 (Read-Only Web):

// Focus: Primarily static content delivery.
// User Interaction: Limited. Users primarily consumed information presented by website creators.
// Examples: Static web pages, online directories, early search engines.
// Technology: Simple HTML pages, basic web servers.
// Web 2.0 (Participative Social Web):

// Focus: User-generated content and interaction.
// User Interaction: High. Users can create content, share information, interact with others, and participate in online communities.
// Examples: Social media platforms (Facebook, Twitter), blogs, wikis, video sharing sites (YouTube), e-commerce platforms.
// Technology: Dynamic HTML, CSS, JavaScript, AJAX, APIs, web applications.
